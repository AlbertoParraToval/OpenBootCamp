// Sentencias Switch
let nota = 5;

switch (nota) {
  case 5:
    console.log("Enhorabuena, has obtenido un sobresaliente")
    break;
  case 4:
    console.log("Podrias haberlo hecho mejor")
    break;

  case 3:
    console.log("Has aprobado raspado")
    break;

  case 2:
    console.log("No has aprobado por poco")
    break;

  case 1:
    console.log("No has estudiado nada")
    break;

  default:
    console.log("Error")
    break;
}
